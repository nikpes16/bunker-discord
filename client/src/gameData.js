function shuffle(array) {
    const arr = [...array];
    for (let i = arr.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [arr[i], arr[j]] = [arr[j], arr[i]];
    }
    return arr;
}

export function dealCards(playerCount) {
    const professions = shuffle(PROFESSIONS);
    const health = shuffle(HEALTH_CONDITIONS);
    const baggage = shuffle(BAGGAGE);
    const hobbies = shuffle(HOBBIES);
    const facts = shuffle(FACTS);
    const specials = shuffle(SPECIAL_CONDITIONS);

    const hands = [];
    for (let i = 0; i < playerCount; i++) {
        hands.push({
            [CARD_TYPES.PROFESSION]: professions[i % professions.length],
            [CARD_TYPES.HEALTH]: health[i % health.length],
            [CARD_TYPES.BAGGAGE]: baggage[i % baggage.length],
            [CARD_TYPES.HOBBY]: hobbies[i % hobbies.length],
            [CARD_TYPES.FACT]: facts[i % facts.length],
            [CARD_TYPES.SPECIAL]: specials[i % specials.length],
        });
    }
    return hands;
}

